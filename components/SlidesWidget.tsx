import React, { useState, useRef } from 'react';
import { ChevronLeft, ChevronRight, Download, Maximize2, X, Image as ImageIcon, Upload } from 'lucide-react';
import { SlidesWidgetData } from '../types';
import { BarChart, Bar, XAxis, YAxis, CartesianGrid, Tooltip, ResponsiveContainer, Cell } from 'recharts';
import PptxGenJS from 'pptxgenjs';

interface SlidesWidgetProps {
  data: SlidesWidgetData;
}

export const SlidesWidget: React.FC<SlidesWidgetProps> = ({ data }) => {
  const [currentIndex, setCurrentIndex] = useState(0);
  const [isFullscreen, setIsFullscreen] = useState(false);
  const [userImages, setUserImages] = useState<Record<number, string>>({});
  const fileInputRef = useRef<HTMLInputElement>(null);

  const nextSlide = () => {
    setCurrentIndex((prev) => (prev + 1) % data.slides.length);
  };

  const prevSlide = () => {
    setCurrentIndex((prev) => (prev - 1 + data.slides.length) % data.slides.length);
  };

  const handleImageUpload = (e: React.ChangeEvent<HTMLInputElement>) => {
    if (e.target.files && e.target.files[0]) {
      const file = e.target.files[0];
      const reader = new FileReader();
      reader.onload = (ev) => {
        if (ev.target?.result) {
          setUserImages(prev => ({ ...prev, [currentIndex]: ev.target!.result as string }));
        }
      };
      reader.readAsDataURL(file);
    }
  };

  const triggerImageUpload = () => {
    fileInputRef.current?.click();
  };

  const downloadPresentation = async () => {
    const pres = new PptxGenJS();
    pres.layout = 'LAYOUT_16x9';

    // Professional White Theme
    const BG_COLOR = 'FFFFFF';
    const TEXT_COLOR = '333333';
    const TITLE_COLOR = '111111';
    const ACCENT_COLOR = '20808D'; // True Turquoise

    // Helper to get image data (Base64 is safest for browser generation)
    const getImageDataForSlide = async (idx: number, url?: string) => {
        if (userImages[idx]) return userImages[idx]; // User uploaded (Data URL)
        if (!url) return null;
        if (url.startsWith('data:')) return url;

        try {
            // Try to fetch to avoid CORS in PPT viewer, but if CORS fails here, fallback to URL
            const response = await fetch(url);
            const blob = await response.blob();
            return await new Promise<string>((resolve) => {
                const reader = new FileReader();
                reader.onloadend = () => resolve(reader.result as string);
                reader.readAsDataURL(blob);
            });
        } catch (e) {
            return url; // Fallback to URL link
        }
    };

    // 1. Title Slide
    let slide = pres.addSlide();
    slide.background = { color: BG_COLOR }; 
    
    // Decorative Bar
    slide.addShape(pres.ShapeType.rect, { x: 0, y: 0, w: '100%', h: 0.15, fill: { color: ACCENT_COLOR } });

    slide.addText(data.title, { 
        x: 0.5, y: 2.5, w: '90%', h: 1.5, 
        fontSize: 44, color: TITLE_COLOR, bold: true, align: 'center', fontFace: 'Arial' 
    });
    slide.addText('Generated by Impersio', { 
        x: 0.5, y: 4.2, w: '90%', h: 0.5, 
        fontSize: 14, color: '666666', align: 'center', fontFace: 'Arial' 
    });

    // 2. Content Slides
    for (let i = 0; i < data.slides.length; i++) {
        const s = data.slides[i];
        slide = pres.addSlide();
        slide.background = { color: BG_COLOR };
        
        // Slide Number
        slide.addText(`${i + 1}`, { x: 9.2, y: 5.1, w: 0.5, h: 0.3, fontSize: 10, color: '999999' });

        // Slide Title
        slide.addText(s.title, { 
            x: 0.5, y: 0.4, w: '90%', h: 0.8, 
            fontSize: 32, color: TITLE_COLOR, bold: true, fontFace: 'Arial' 
        });

        // Separator
        slide.addShape(pres.ShapeType.line, { x: 0.5, y: 1.2, w: '90%', h: 0, line: { color: ACCENT_COLOR, width: 2 } });

        const hasChart = !!s.chart;
        const imgData = await getImageDataForSlide(i, s.image);
        const hasImage = !!imgData;
        
        let textWidth = '90%';
        let contentX = 0.5;

        // Layout Logic
        if (hasChart || hasImage) {
            textWidth = '50%';
        }

        // Bullet Points
        const bullets = s.content.map(text => ({ 
            text, 
            options: { 
                fontSize: 18, 
                color: TEXT_COLOR, 
                breakLine: true, 
                bullet: { type: 'bullet' as const, characterCode: '2022' }, 
                paraSpaceBefore: 12,
                lineSpacing: 28
            } 
        }));
        
        slide.addText(bullets, { 
            x: contentX, y: 1.5, w: textWidth, h: 3.5, 
            align: 'left', valign: 'top', fontFace: 'Arial' 
        });

        // Add Chart
        if (hasChart && s.chart) {
             const chartData = [
                {
                    name: s.chart.title || "Data",
                    labels: s.chart.data.map(d => d.label),
                    values: s.chart.data.map(d => d.value)
                }
             ];
             slide.addChart(pres.ChartType.bar, chartData, { 
                 x: 5.8, y: 1.5, w: 3.8, h: 3, 
                 chartColors: [ACCENT_COLOR],
                 valAxisLabelColor: '666666',
                 catAxisLabelColor: '666666',
                 showTitle: true,
                 title: s.chart.title || "Data Analysis"
             });
        }

        // Add Image
        if (hasImage && imgData && !hasChart) {
            try {
                slide.addImage({ 
                    data: imgData.startsWith('data:') ? imgData : undefined,
                    path: !imgData.startsWith('data:') ? imgData : undefined,
                    x: 5.8, y: 1.5, w: 3.8, h: 3,
                    sizing: { type: 'contain', w: 3.8, h: 3 }
                });
            } catch (e) {
                console.error("Failed to add image to PPT", e);
            }
        }
    }

    pres.writeFile({ fileName: `${data.title.replace(/[^a-zA-Z0-9]/g, '_')}.pptx` });
  };

  const currentSlide = data.slides[currentIndex];
  const activeImage = userImages[currentIndex] || currentSlide.image;

  const RenderContent = () => (
    <div key={currentIndex} className="absolute inset-0 p-6 md:p-8 flex flex-col bg-surface animate-in fade-in duration-300">
        <div className="flex justify-between items-start mb-6">
            <h2 className="text-2xl md:text-3xl font-bold text-scira-accent leading-tight truncate flex-1 pr-4">
                {currentSlide.title}
            </h2>
             {/* Slide specific actions */}
             <div className="flex gap-2">
                 <button 
                   onClick={triggerImageUpload}
                   className="p-2 rounded-lg bg-surface-hover hover:bg-border text-muted hover:text-primary transition-colors"
                   title="Add/Replace Image"
                 >
                   <ImageIcon className="w-4 h-4" />
                   <input 
                     type="file" 
                     ref={fileInputRef} 
                     onChange={handleImageUpload} 
                     className="hidden" 
                     accept="image/*"
                   />
                 </button>
             </div>
        </div>
        
        <div className="flex-1 flex gap-6 min-h-0">
            {/* Text Content */}
            <div className={`flex-1 overflow-y-auto ${currentSlide.chart || activeImage ? 'w-full md:w-1/2' : 'w-full'} pr-2`}>
                <ul className="space-y-4">
                    {currentSlide.content.map((point, idx) => (
                        <li key={idx} className="flex items-start gap-3 text-lg text-primary/90">
                            <span className="mt-2.5 w-1.5 h-1.5 rounded-full bg-scira-accent shrink-0" />
                            <span className="leading-relaxed">{point}</span>
                        </li>
                    ))}
                </ul>
            </div>

            {/* Right Side: Chart or Image */}
            {(currentSlide.chart || activeImage) && (
                <div className="hidden md:flex w-1/2 h-full items-center justify-center">
                    {currentSlide.chart ? (
                        <div className="w-full h-full max-h-[300px] bg-background/50 rounded-xl p-4 border border-border">
                            <h3 className="text-sm font-semibold text-muted mb-4 uppercase tracking-wider">{currentSlide.chart.title || "Data Analysis"}</h3>
                            <ResponsiveContainer width="100%" height="100%">
                                <BarChart data={currentSlide.chart.data}>
                                    <CartesianGrid strokeDasharray="3 3" stroke="#333" vertical={false} />
                                    <XAxis 
                                        dataKey="label" 
                                        stroke="#71717a" 
                                        tick={{fill: '#71717a', fontSize: 12}} 
                                        tickLine={false} 
                                        axisLine={false}
                                        interval={0}
                                    />
                                    <YAxis 
                                        stroke="#71717a" 
                                        tick={{fill: '#71717a', fontSize: 12}} 
                                        tickLine={false} 
                                        axisLine={false} 
                                    />
                                    <Tooltip 
                                        contentStyle={{backgroundColor: '#18181b', borderColor: '#27272a', color: '#fff'}}
                                        itemStyle={{color: '#fff'}}
                                        cursor={{fill: '#27272a', opacity: 0.5}}
                                    />
                                    <Bar dataKey="value" radius={[4, 4, 0, 0]}>
                                        {currentSlide.chart.data.map((entry, index) => (
                                            <Cell key={`cell-${index}`} fill={index % 2 === 0 ? '#20808D' : '#A84B2F'} />
                                        ))}
                                    </Bar>
                                </BarChart>
                            </ResponsiveContainer>
                        </div>
                    ) : activeImage ? (
                        <div className="w-full h-full max-h-[300px] rounded-xl overflow-hidden border border-border relative group/img">
                            <img 
                                src={activeImage} 
                                alt={currentSlide.title} 
                                className="w-full h-full object-cover"
                            />
                            {/* Hover overlay to change image */}
                            <div className="absolute inset-0 bg-black/40 opacity-0 group-hover/img:opacity-100 transition-opacity flex items-center justify-center">
                                <button onClick={triggerImageUpload} className="px-4 py-2 bg-white/10 backdrop-blur-md rounded-full text-white font-medium flex items-center gap-2 hover:bg-white/20">
                                    <Upload className="w-4 h-4" /> Change
                                </button>
                            </div>
                        </div>
                    ) : null}
                </div>
            )}
        </div>
    </div>
  );

  return (
    <div className={`
        relative w-full max-w-3xl bg-surface border border-border rounded-2xl overflow-hidden mb-8 animate-fade-in transition-all duration-300
        ${isFullscreen ? 'fixed inset-0 z-50 rounded-none max-w-none h-full' : 'aspect-video'}
    `}>
       {/* Toolbar */}
       <div className="absolute top-4 right-4 z-20 flex items-center gap-2">
          <button 
            onClick={downloadPresentation}
            className="p-2 rounded-lg bg-surface/80 hover:bg-surface-hover backdrop-blur-sm text-muted hover:text-primary transition-colors border border-border/50"
            title="Download PPT"
          >
            <Download className="w-4 h-4" />
          </button>
          <button 
            onClick={() => setIsFullscreen(!isFullscreen)}
            className="p-2 rounded-lg bg-surface/80 hover:bg-surface-hover backdrop-blur-sm text-muted hover:text-primary transition-colors border border-border/50"
            title={isFullscreen ? "Exit Fullscreen" : "Fullscreen"}
          >
            {isFullscreen ? <X className="w-4 h-4" /> : <Maximize2 className="w-4 h-4" />}
          </button>
       </div>

       {/* Slide Content */}
       <RenderContent />

       {/* Navigation */}
       <div className="absolute bottom-4 left-0 right-0 z-20 flex justify-center items-center gap-4">
           <button 
             onClick={prevSlide}
             className="p-2 rounded-full bg-surface/80 hover:bg-surface-hover backdrop-blur-sm text-primary transition-colors border border-border/50 shadow-sm"
           >
              <ChevronLeft className="w-5 h-5" />
           </button>
           <span className="text-sm font-medium text-primary/80 bg-surface/50 px-3 py-1 rounded-full backdrop-blur-sm select-none">
              {currentIndex + 1} / {data.slides.length}
           </span>
           <button 
             onClick={nextSlide}
             className="p-2 rounded-full bg-surface/80 hover:bg-surface-hover backdrop-blur-sm text-primary transition-colors border border-border/50 shadow-sm"
           >
              <ChevronRight className="w-5 h-5" />
           </button>
       </div>
    </div>
  );
};